# Variáveis em Python
# Não há necessidade de declará-las no início do programa
# Basta criar no meio do algoritmo com a aribuição

teste = "Amanda"
print(teste) # Imprime a string "Eduardo"
print(type(teste)) # Imprime o tipo do variável, string
teste = 10
print(teste) # Imprime o número 10
print(type(teste)) # Imprime o tipo da variável, que é int
teste = 1.74 
print(teste) # Imprime o tipo da variável, que é float
teste = True
print(teste) # Imprime o valor True
print(type(teste)) # Imprime o tipo variável, que é boll